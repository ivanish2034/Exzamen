    /**
     * @param args the command line arguments
     */
    public void updateTasksList() {
        DefaultListModel<Task> listModel = new DefaultListModel<>();
        for (Task task : tasks) {
            listModel.addElement(task);
        }
        taskList.setModel(listModel);
    }
    
